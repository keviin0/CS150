// Add2Numbers.cpp - Add two whole numbers together and print the results
// Author: Chris Merrill

#include <iostream>
using namespace std ;

int main()
{
    // Create three int variables to add using random values
    int a = 52 ;
    int b = 39 ;
    int c = 28 ;

    // Calculate their sum and print the result
    int sum = a + b + c;
    cout << "The sum of " << a << ", " << b << ", and "  << c << " is " << sum << "." << endl ;

    return 0 ;
}
